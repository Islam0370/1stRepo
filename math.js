
Math.sqrt(16); // Returns the square root of 16
Math.pow(2, 3); // Returns 2 raised to the power of 3
Math.random(); // Returns a random number between 0 (inclusive) and 1 (exclusive)
Math.PI; // Returns the value of π (pi)
Math.round(4.7); // Rounds 4.7 to the nearest integer (5)
Math.floor(4.7); // Rounds 4.7 down to the nearest integer (4)
Math.ceil(4.3); // Rounds 4.3 up to the nearest integer (5)         
// Math is a built-in object in JavaScript